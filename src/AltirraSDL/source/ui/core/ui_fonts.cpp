static bool EndsWithCI(const VDStringA& s, const char *suffix) {
	const size_t n = strlen(suffix);
	if (s.size() < n) return false;
	for (size_t i = 0; i < n; ++i) {
		char a = (char)tolower((unsigned char)s[s.size() - n + i]);
		char b = (char)tolower((unsigned char)suffix[i]);
		if (a != b) return false;
	}
	return true;
}

// Parse a filename like "Roboto-MediumItalic.ttf" or "FiraMono-Regular.ttf"
// into family / weight / italic.  Variable-font filenames (those with
// "VariableFont" in them) are skipped — ImGui's stb_truetype loader does
// not honor variation axes, so the static instances are preferred.
static bool ParseFontFile(const VDStringA& filename, ATFontFile& out) {
	if (!EndsWithCI(filename, ".ttf") && !EndsWithCI(filename, ".otf"))
		return false;

	VDStringA stem(filename);
	stem.resize(stem.size() - 4);

	if (strstr(stem.c_str(), "VariableFont") || strstr(stem.c_str(), "variableFont"))
		return false;

	// Family is everything before the first '-'; suffix after is the style.
	const char *dash = strchr(stem.c_str(), '-');
	VDStringA family;
	VDStringA suffix;
	if (dash) {
		family.assign(stem.c_str(), dash - stem.c_str());
		suffix = dash + 1;
	} else {
		family = stem;
		suffix = "Regular";
	}

	// Pretty-print common family names: "FiraMono" -> "Fira Mono",
	// "Roboto_Condensed" -> "Roboto Condensed".
	{
		VDStringA pretty;
		for (size_t i = 0; i < family.size(); ++i) {
			char c = family[i];
			if (c == '_') {
				pretty += ' ';
				continue;
			}
			if (i > 0 && isupper((unsigned char)c) &&
				islower((unsigned char)family[i-1]) &&
				pretty.size() && pretty[pretty.size()-1] != ' ')
			{
				pretty += ' ';
			}
			pretty += c;
		}
		family = pretty;
	}

	bool italic = false;
	if (const char *it = strstr(suffix.c_str(), "Italic")) {
		italic = true;
		// strip "Italic" from suffix
		size_t pos = it - suffix.c_str();
		suffix.erase(pos, 6);
		if (suffix.empty()) suffix = "Regular";
	}

	out.family = family;
	out.weight = suffix;
	out.italic = italic;
	out.monospaced = (strstr(family.c_str(), "Mono") != nullptr) ||
	                 (strstr(family.c_str(), "mono") != nullptr);
	return true;
}

static void ScanFontDir(const VDStringA& dir) {
	// Recursive scan: top-level *.ttf, then any one-level subdirs (e.g.
	// Roboto/static/, Fira_Mono/).  We avoid the heavyweight VDDirectoryIterator
	// dependency and use SDL's enumerator since SDL3 is already linked.
	struct Ctx {
		VDStringA base;
		void Scan(const VDStringA& d) {
			SDL_EnumerateDirectoryCallback cb = [](void *ud, const char *dirname, const char *fname) -> SDL_EnumerationResult {
				auto *self = (Ctx *)ud;
				VDStringA full = VDStringA(dirname);
				if (!full.empty() && full[full.size()-1] != '/' && full[full.size()-1] != '\\')
					full += '/';
				full += fname;

				SDL_PathInfo info {};
				if (!SDL_GetPathInfo(full.c_str(), &info))
					return SDL_ENUM_CONTINUE;

				if (info.type == SDL_PATHTYPE_DIRECTORY) {
					self->Scan(full);
				} else if (info.type == SDL_PATHTYPE_FILE) {
					ATFontFile rec;
					if (ParseFontFile(VDStringA(fname), rec)) {
						rec.path = full;
						s_fontFiles.push_back(std::move(rec));
					}
				}
				return SDL_ENUM_CONTINUE;
			};
			SDL_EnumerateDirectory(d.c_str(), cb, this);
		}
	} ctx;
	ctx.base = dir;
	ctx.Scan(dir);
}

static void RebuildFamilyLists() {
	s_uiFamilies.clear();
	s_monoFamilies.clear();
	for (const auto& f : s_fontFiles) {
		auto& target = f.monospaced ? s_monoFamilies : s_uiFamilies;
		if (std::find(target.begin(), target.end(), f.family) == target.end())
			target.push_back(f.family);
	}
	std::sort(s_uiFamilies.begin(), s_uiFamilies.end());
	std::sort(s_monoFamilies.begin(), s_monoFamilies.end());
	// If there are no monospaced families discovered, fall back to UI list
	// so the user always has *something* to pick.
	if (s_monoFamilies.empty())
		s_monoFamilies = s_uiFamilies;
}

// Pick the best file for the requested (family, weight) pair.  Falls back
// to Regular weight, then any file in the family, then any file at all.
static const ATFontFile *PickFontFile(const char *family, const char *weight) {
	const ATFontFile *exact = nullptr, *familyRegular = nullptr, *anyInFamily = nullptr;
	for (const auto& f : s_fontFiles) {
		if (f.italic) continue;
		if (f.family != family) continue;
		if (!anyInFamily) anyInFamily = &f;
		if (f.weight == "Regular") familyRegular = &f;
		if (f.weight == weight) { exact = &f; break; }
	}
	if (exact) return exact;
	if (familyRegular) return familyRegular;
	if (anyInFamily) return anyInFamily;
	return s_fontFiles.empty() ? nullptr : &s_fontFiles[0];
}

