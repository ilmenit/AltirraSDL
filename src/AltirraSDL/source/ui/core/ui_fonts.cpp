namespace {
	constexpr const char *kDefaultUIFamily   = "Roboto";
	constexpr const char *kDefaultUIWeight   = "Medium";
	constexpr int         kDefaultUISize     = 11;   // points
	constexpr const char *kDefaultMonoFamily = "Fira Mono";
	constexpr const char *kDefaultMonoWeight = "Regular";
	constexpr int         kDefaultMonoSize   = 10;
}

static VDStringA s_uiFamily(kDefaultUIFamily);
static VDStringA s_uiWeight(kDefaultUIWeight);
static int       s_uiSize     = kDefaultUISize;

static VDStringA s_monoFamily(kDefaultMonoFamily);
static VDStringA s_monoWeight(kDefaultMonoWeight);
static int       s_monoSize   = kDefaultMonoSize;

static float s_contentScale = 1.0f;
static bool  s_dirty = false;
static bool  s_initialized = false;
static bool  s_usingGLBackend = false;

ImFont *g_pFontUI   = nullptr;
ImFont *g_pFontMono = nullptr;

