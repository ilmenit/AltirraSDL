class ATSimulator;

static int FindIndex(const std::vector<VDStringA>& v, const VDStringA& s) {
	for (size_t i = 0; i < v.size(); ++i)
		if (v[i] == s) return (int)i;
	return -1;
}

// Lists weight names available for a given family (excluding italic variants).
static std::vector<VDStringA> CollectWeights(const char *family) {
	std::vector<VDStringA> out;
	for (const auto& f : s_fontFiles) {
		if (f.italic) continue;
		if (f.family != family) continue;
		if (std::find(out.begin(), out.end(), f.weight) == out.end())
			out.push_back(f.weight);
	}
	std::sort(out.begin(), out.end());
	return out;
}

static void RenderFamilyCombo(const char *label,
	const std::vector<VDStringA>& families,
	VDStringA& currentFamily,
	VDStringA& currentWeight)
{
	int idx = FindIndex(families, currentFamily);
	std::vector<const char *> items;
	items.reserve(families.size());
	for (const auto& f : families) items.push_back(f.c_str());

	if (families.empty()) {
		ImGui::TextDisabled("(no fonts found in fonts/ directory)");
		return;
	}

	if (idx < 0) idx = 0;
	if (ImGui::Combo(label, &idx, items.data(), (int)items.size())) {
		currentFamily = families[idx];
		// Reset weight to a valid choice for the new family
		auto weights = CollectWeights(currentFamily.c_str());
		if (std::find(weights.begin(), weights.end(), currentWeight) == weights.end())
			currentWeight = weights.empty() ? VDStringA("Regular") : weights.front();
		s_dirty = true;
	}
}

static void RenderWeightCombo(const char *label, const char *family, VDStringA& currentWeight) {
	auto weights = CollectWeights(family);
	if (weights.empty()) return;

	std::vector<const char *> items;
	items.reserve(weights.size());
	for (const auto& w : weights) items.push_back(w.c_str());

	int idx = -1;
	for (size_t i = 0; i < weights.size(); ++i)
		if (weights[i] == currentWeight) { idx = (int)i; break; }
	if (idx < 0) {
		idx = 0;
		currentWeight = weights[0];
	}
	if (ImGui::Combo(label, &idx, items.data(), (int)items.size())) {
		currentWeight = weights[idx];
		s_dirty = true;
	}
}

void RenderFontsCategory(ATSimulator &) {
	ImGui::TextWrapped(
		"Choose the fonts used by the Altirra UI.  TTF/OTF files placed in the "
		"\"fonts\" directory next to the executable are picked up automatically. "
		"Changes apply immediately and are saved on exit.");
	ImGui::Spacing();

	// ----- UI Font -----
	ImGui::SeparatorText("UI Font");
	ImGui::TextDisabled("Used for menus, dialogs, and text in the emulator UI.");
	RenderFamilyCombo("Family##ui", s_uiFamilies, s_uiFamily, s_uiWeight);
	RenderWeightCombo("Weight##ui", s_uiFamily.c_str(), s_uiWeight);
	if (ImGui::SliderInt("Size (pt)##ui", &s_uiSize, 8, 32, "%d pt")) {
		s_dirty = true;
	}
	ImGui::SetItemTooltip("Drag or click; the change applies on the next frame.");

	// Live preview using the currently active font (rebuilt at end of frame).
	if (g_pFontUI) {
		ImGui::PushFont(g_pFontUI);
		ImGui::TextUnformatted("Preview: The quick brown fox jumps over the lazy dog 0123456789");
		ImGui::PopFont();
	}
	ImGui::Spacing();

	// ----- Monospace Font -----
	ImGui::SeparatorText("Monospace Font");
	ImGui::TextDisabled(
		"Used for the debugger panes (disassembly, memory, history, console).");
	RenderFamilyCombo("Family##mono", s_monoFamilies, s_monoFamily, s_monoWeight);
	RenderWeightCombo("Weight##mono", s_monoFamily.c_str(), s_monoWeight);
	if (ImGui::SliderInt("Size (pt)##mono", &s_monoSize, 8, 32, "%d pt")) {
		s_dirty = true;
	}

	if (g_pFontMono) {
		ImGui::PushFont(g_pFontMono);
		ImGui::TextUnformatted("Preview: lda $D01A   ; 0xDEADBEEF  iiii  WWWW  ....");
		ImGui::PopFont();
	}
	ImGui::Spacing();

	// ----- Reset -----
	ImGui::Separator();
	if (ImGui::Button("Reset to Defaults")) {
		s_uiFamily   = kDefaultUIFamily;
		s_uiWeight   = kDefaultUIWeight;
		s_uiSize     = kDefaultUISize;
		s_monoFamily = kDefaultMonoFamily;
		s_monoWeight = kDefaultMonoWeight;
		s_monoSize   = kDefaultMonoSize;
		s_dirty = true;
	}
	ImGui::SameLine();
	ImGui::TextDisabled("Roboto Medium 16 pt / Fira Mono Regular 14 pt");
}
