void ATUIFontsInit(float contentScale, bool usingGLBackend) {
	s_contentScale = contentScale > 0 ? contentScale : 1.0f;
	s_usingGLBackend = usingGLBackend;
	s_initialized = true;

	// Discover fonts: embedded blobs first (always present, never fails),
	// then <exe_dir>/fonts/ for user-supplied additions, then CWD fallback.
	s_fontFiles.clear();

	// --- Embedded fonts (always available) ---
	{
		ATFontFile rec;
		rec.family       = "Roboto";
		rec.weight       = "Medium";
		rec.italic       = false;
		rec.monospaced   = false;
		rec.path         = "<builtin:roboto-medium>";
		rec.mpBuiltinData = g_atFontRobotoMediumData;
		rec.mBuiltinSize  = g_atFontRobotoMediumData_size;
		s_fontFiles.push_back(rec);

		rec.family       = "Fira Mono";
		rec.weight       = "Regular";
		rec.monospaced   = true;
		rec.path         = "<builtin:firamono-regular>";
		rec.mpBuiltinData = g_atFontFiraMonoRegularData;
		rec.mBuiltinSize  = g_atFontFiraMonoRegularData_size;
		s_fontFiles.push_back(rec);
	}

	// --- User-supplied additions: scan <exe>/fonts/, then CWD/fonts/. ---
	// Embedded fonts already cover the default experience; this is purely
	// for users who want extra families.  Both scans are best-effort.
	const size_t builtinCount = s_fontFiles.size();
	const char *base = SDL_GetBasePath();
	if (base && *base) {
		VDStringA dir(base);
		dir += "fonts";
		ScanFontDir(dir);
	}
	if (s_fontFiles.size() == builtinCount) {
		ScanFontDir(VDStringA("fonts"));
	}

	RebuildFamilyLists();
	LoadFromRegistry();

	LOG_INFO("UI", "Fonts: discovered %zu files, %zu UI families, %zu mono families",
		s_fontFiles.size(), s_uiFamilies.size(), s_monoFamilies.size());

	RebuildAtlas();
	s_dirty = false;
}

void ATUIFontsRebuildIfDirty() {
	if (!s_dirty || !s_initialized) return;
	s_dirty = false;

	// Tear down the current backend texture so the next NewFrame uploads
	// a fresh one built from the new atlas.
	if (s_usingGLBackend) {
		ImGui_ImplOpenGL3_DestroyFontsTexture();
	} else {
		ImGui_ImplSDLRenderer3_DestroyFontsTexture();
	}

	RebuildAtlas();
	SaveToRegistry();
}

ImFont *ATUIGetFontUI() {
	if (g_pFontUI)
		return g_pFontUI;
	return ImGui::GetIO().FontDefault;
}

ImFont *ATUIGetFontMono() {
	if (g_pFontMono)
		return g_pFontMono;
	if (g_pFontUI)
		return g_pFontUI;
	return ImGui::GetIO().FontDefault;
}

