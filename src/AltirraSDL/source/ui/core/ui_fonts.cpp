// Embedded font blobs — generated by cmake/embed_font.cmake at build time.
// Always linked into the binary so the UI works on every platform without
// depending on a runtime fonts/ directory.
extern "C" {
	extern const unsigned char g_atFontRobotoMediumData[];
	extern const unsigned int  g_atFontRobotoMediumData_size;
	extern const unsigned char g_atFontFiraMonoRegularData[];
	extern const unsigned int  g_atFontFiraMonoRegularData_size;
}

// =========================================================================
// Discovered font records
// =========================================================================

struct ATFontFile {
	VDStringA family;     // e.g. "Roboto", "Fira Mono"
	VDStringA weight;     // e.g. "Regular", "Medium", "Bold", "Light", ...
	bool italic = false;
	bool monospaced = false;
	// Either a filesystem path, OR a "<builtin>" marker.  When mpBuiltinData
	// is non-null, the font is loaded from memory instead of disk and `path`
	// is just a human-readable label.
	VDStringA path;
	const unsigned char *mpBuiltinData = nullptr;
	unsigned int mBuiltinSize = 0;
};

static std::vector<ATFontFile> s_fontFiles;
static std::vector<VDStringA> s_uiFamilies;    // distinct, proportional first
static std::vector<VDStringA> s_monoFamilies;  // distinct, monospaced

