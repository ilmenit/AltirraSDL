//	AltirraSDL - Font management for the Dear ImGui UI
//
//	Discovers TTF/OTF files under <exe>/fonts/ (or CWD/fonts/), exposes a
//	user-facing UI font and a separate monospace font, persists the choice
//	to the registry under Windows-compatible keys (Console: Font* for the
//	mono font; UI: Font* for the UI font), and rebuilds the ImGui font
//	atlas at end-of-frame whenever the user changes a setting.
//
//	The Configure System "Fonts" page (RenderFontsCategory) lives in this
//	file as well so all font logic stays in one place.

