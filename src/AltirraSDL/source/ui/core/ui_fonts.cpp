static const char *kRegPath = "Settings";

static void LoadFromRegistry() {
	VDRegistryAppKey key(kRegPath, false);

	VDStringW tmp;
	if (key.getString("UI: Font family", tmp))
		s_uiFamily = VDTextWToA(tmp);
	if (key.getString("UI: Font weight", tmp))
		s_uiWeight = VDTextWToA(tmp);
	int v = key.getInt("UI: Font point size tenths", 0);
	if (v > 0) s_uiSize = v / 10;

	if (key.getString("Console: Font family", tmp))
		s_monoFamily = VDTextWToA(tmp);
	if (key.getString("Console: Font weight", tmp))
		s_monoWeight = VDTextWToA(tmp);
	v = key.getInt("Console: Font point size tenths", 0);
	if (v > 0) s_monoSize = v / 10;
}

static void SaveToRegistry() {
	VDRegistryAppKey key(kRegPath, true);
	key.setString("UI: Font family", VDTextAToW(s_uiFamily.c_str()).c_str());
	key.setString("UI: Font weight", VDTextAToW(s_uiWeight.c_str()).c_str());
	key.setInt("UI: Font point size tenths", s_uiSize * 10);

	key.setString("Console: Font family", VDTextAToW(s_monoFamily.c_str()).c_str());
	key.setString("Console: Font weight", VDTextAToW(s_monoWeight.c_str()).c_str());
	key.setInt("Console: Font point size tenths", s_monoSize * 10);
}

