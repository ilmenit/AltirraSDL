// Build a static glyph range covering Latin-1, Latin Extended, Greek, and
// Cyrillic via ImFontGlyphRangesBuilder.  Persistent storage is required
// because ImGui keeps the pointer until the atlas is built.
static const ImWchar *GetUIFontGlyphRanges() {
	static ImVector<ImWchar> ranges;
	if (ranges.empty()) {
		ImFontGlyphRangesBuilder builder;
		ImFontAtlas *atlas = ImGui::GetIO().Fonts;
		builder.AddRanges(atlas->GetGlyphRangesDefault());   // Basic Latin + Latin-1
		builder.AddRanges(atlas->GetGlyphRangesCyrillic());  // ~400 Cyrillic glyphs
		builder.AddRanges(atlas->GetGlyphRangesGreek());     // Greek + Coptic
		// Latin Extended-A and Extended-B (Western/Central European accents
		// not covered by Latin-1: ą ć ę ł ń ś ź ż ř š č etc.)
		static const ImWchar kLatinExt[] = { 0x0100, 0x024F, 0 };
		builder.AddRanges(kLatinExt);
		// General Punctuation (en-dash, em-dash, smart quotes, ellipsis,
		// bullet, trade/service marks) — common in game pack file names.
		static const ImWchar kPunctuation[] = { 0x2000, 0x206F, 0 };
		builder.AddRanges(kPunctuation);
		// Letterlike Symbols (trademark ™ etc.)
		static const ImWchar kLetterlike[] = { 0x2100, 0x214F, 0 };
		builder.AddRanges(kLetterlike);
		builder.BuildRanges(&ranges);
	}
	return ranges.Data;
}

// Mono font: keep the atlas small — debugger panes display ASCII + a few
// box-drawing chars.  Default range (Basic Latin + Latin-1) is enough.
static const ImWchar *GetMonoFontGlyphRanges() {
	return ImGui::GetIO().Fonts->GetGlyphRangesDefault();
}

static void LoadFontFromFile(const ATFontFile *file, float pixelSize,
	const ImWchar *glyphRanges, ImFont*& out)
{
	out = nullptr;
	if (!file) return;

	const void *srcData = nullptr;
	size_t srcSize = 0;
	void *sdlData = nullptr;

	if (file->mpBuiltinData) {
		// Embedded blob — point at the linker-provided data directly.
		srcData = file->mpBuiltinData;
		srcSize = file->mBuiltinSize;
	} else {
		sdlData = SDL_LoadFile(file->path.c_str(), &srcSize);
		if (!sdlData || srcSize == 0) {
			LOG_INFO("UI", "Font load failed: %s", file->path.c_str());
			if (sdlData) SDL_free(sdlData);
			return;
		}
		srcData = sdlData;
	}

	// Always copy through ImGui's allocator so FontDataOwnedByAtlas can
	// safely IM_FREE() it later.  Embedded data must NOT be freed; copying
	// keeps that ownership invariant simple.
	void *imgData = IM_ALLOC(srcSize);
	memcpy(imgData, srcData, srcSize);
	if (sdlData) SDL_free(sdlData);

	ImFontConfig cfg;
	cfg.FontDataOwnedByAtlas = true;

	ImGuiIO& io = ImGui::GetIO();
	out = io.Fonts->AddFontFromMemoryTTF(imgData, (int)srcSize, pixelSize, &cfg, glyphRanges);
}

static void RebuildAtlas() {
	ImGuiIO& io = ImGui::GetIO();
	io.Fonts->Clear();

	const float uiPx   = (float)s_uiSize   * s_contentScale * 96.0f / 72.0f;
	const float monoPx = (float)s_monoSize * s_contentScale * 96.0f / 72.0f;

	LOG_INFO("UI", "Font rebuild: uiSize=%d monoSize=%d contentScale=%.2f uiPx=%.1f monoPx=%.1f FontGlobalScale=%.2f",
		s_uiSize, s_monoSize, s_contentScale, uiPx, monoPx, ImGui::GetIO().FontGlobalScale);

	const ATFontFile *uiFile   = PickFontFile(s_uiFamily.c_str(),   s_uiWeight.c_str());
	const ATFontFile *monoFile = PickFontFile(s_monoFamily.c_str(), s_monoWeight.c_str());

	LoadFontFromFile(uiFile,   uiPx,   GetUIFontGlyphRanges(),   g_pFontUI);
	LoadFontFromFile(monoFile, monoPx, GetMonoFontGlyphRanges(), g_pFontMono);

	if (!g_pFontUI) {
		// Last-resort fallback: ImGui's built-in ProggyClean bitmap font.
		// Reached only if every TTF (including the embedded one) failed
		// to load, e.g. due to memory pressure or a corrupted blob.
		LOG_INFO("UI", "All TTF fonts failed to load — using ProggyClean fallback");
		g_pFontUI = io.Fonts->AddFontDefault();
	}
	if (!g_pFontMono) {
		g_pFontMono = g_pFontUI;
	}

	io.FontDefault = g_pFontUI;
	io.Fonts->Build();
}

